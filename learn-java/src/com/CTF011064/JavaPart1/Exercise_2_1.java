public class Exercise_2_1 {

	public static void main(String[] args) {

		// Declare variables

		int myIntNum = 100;
		float myFloatNum = 2.50f; // alternative way : (float) 2.50
		boolean myBool = true;
		String myText = "Hello"; // double quoted
		char myLetter = 'A'; // single quoted

		/*
		 * Print each variable out,
		 * Let's try below
		 */

		System.out.println("This is an integer number, " + myIntNum);
		System.out.println("This is a float number, " + myFloatNum);
		System.out.println("This is a boolean, " + myBool);
		System.out.println("This is a String, " + myText);
		System.out.println("This is a character, " + myLetter);
	}
}